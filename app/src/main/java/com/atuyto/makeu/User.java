package com.atuyto.makeu;

public class User {

    public String name, email, first_name, phone, weight, size, sex;


    public User(){}

    public User(String name, String First_name, String email, String Phone, String weight, String size, String sex){

        this.name = name;
        this.first_name = First_name;
        this.email = email;
        this.phone = Phone;
        this.weight = weight;
        this.size = size;
        this.sex = sex;



    }
}
